function validateRadio (radios) {
  for (i = 0; i < radios.length; ++ i) {
    if (radios [i].checked) return true;
  }
  return false;
}

function validateForm() {
  var a = document.getElementById('course-quiz-session');
  var b = document.getElementById('quiz-pass');
  var c = document.getElementById('bad-response');
  var d = document.getElementById('good-response');
  var w = document.forms["survey"]["q1"].value;
  var x = document.forms["survey"]["q2"].value;
  var y = document.forms["survey"]["q3"].value;
  var z = document.forms["survey"]["q4"].value;

  if (w && x && y && z == "good") {
    a.style.display = 'none';
    d.style.display = 'block';
      return false;
  } else if (w && x && y && z == "bad") {
    a.style.display = 'none';
    c.style.display = 'block';
      return false;
  } else {
    a.style.display = 'none';
    b.style.display = 'block';
      return false;
  }
}
